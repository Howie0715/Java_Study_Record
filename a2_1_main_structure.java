package a2_;
public class a2_1_main_structure { // 定義類別，需要和"檔案名稱相同"
    // 只能有一個public類別
    public static void main(String args[]) { // main函數, 主程式
                    //args接收傳入程式的引數  // void=無傳回值
        // 程式內容
        System.out.println("HelloWorld");
        System.out.println(args[0]+"+"+args[1]);
        //輸出括號內的引號內的文字，輸出變數不須引號

    }
}
